<H2><CENTER>Welcome!</CENTER></H2>
<H2><CENTER> What is CP2K?</CENTER></H2>

CP2K is a <A HREF="download.html">freely available</A> (GPL) program, written in Fortran 95, 
to perform atomistic and molecular simulations of solid state, liquid,
molecular and biological systems. It provides a general framework for different methods such as e.g.
density functional theory (DFT) using a mixed Gaussian and plane waves approach (GPW), 
and classical pair and many-body potentials.

<H3> Strengths ... </H3>

CP2K provides state-of-the-art methods for efficient and accurate atomistic simulations,
sources are freely available and actively improved. It is therefore easy to give the code a try, and
to make modifications as needed.

<H3> ... and limitations </H3> 

However, you'll find all versions of the CP2K code on this server and they all come with little documentation and without any warranty. 
No official release has been made.  You might therefore expect that substantial changes, improvements and bug fixes will
be made at irregular intervals. Using the code for production quality simulations is possible but requires detailed 
knowledge about the active development.

<H3> Some key parts of CP2K </H3>

<UL>
<LI> Fist: classical molecular dynamics
<LI> Kim-Gordon Models
<LI> <A HREF="quickstep.html">Quickstep</A>: Density functional calculations
</UL>

